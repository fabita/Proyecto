    void cargarAccesorios(String valor){
        
        String []titulos={"ID", "Descripcion","Fabricante","Precio"};  
        String []Registros= new String[6];
        
        String sql="SELECT * FROM accesorios WHERE CONCAT(id, descripcion, fabricante, precio) LIKE '%"+valor+"%'";
        model=new DefaultTableModel(null,titulos);
        
        try {
             Statement st = cn.createStatement();
             ResultSet rs = st.executeQuery(sql);
             while(rs.next())
             {
                   Registros[0]= rs.getString("id");
                   Registros[1]= rs.getString("descripcion");
                   Registros[2]= rs.getString("fabricante");
                   Registros[3]= rs.getString("precio");
                   model.addRow(Registros);
             } 
             t_datos.setModel(model);
        } catch (SQLException ex) {
             Logger.getLogger(BD_Aditivos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
